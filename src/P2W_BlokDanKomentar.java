public class P2W_BlokDanKomentar {
    public static void main(String[] args) {
        //while loop
        int x = 0;
        System.out.println("Example of while loop--");
        while (x<= 10){
            System.out.printf("%d ",x);
            x++;
        }
        System.out.print('\n');
        //do-while loop
        int y=0;
        do{
            y=y+10;
            System.out.println("Iam inside Do Block---"+y);
        }while(y<=50);
    }
}
